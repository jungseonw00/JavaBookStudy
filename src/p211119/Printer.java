package p211119;

public class Printer {
	public void println(String msg) {
		System.out.println(msg);
	}
	public void println(int msg) {
		System.out.println(msg);
	}
	public void println(boolean msg) {
		System.out.println(msg);
	}
	public void println(double msg) {
		System.out.println(msg);
	}
}
